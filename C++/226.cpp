struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

class Solution {
public:
    TreeNode* invertTree(TreeNode* root) {
        if(root){
            root->left = invertTree(root->right);
            root->right = invertTree(root->left);
        }
        else{
            return NULL;
        }
        return root;
    }
};